public class SquareSpaceLocation {
	private String name;
	private double lat;
	private double lon;
	private String address;
	
	public SquareSpaceLocation(String name, double lat, double lon, String address) {	
		this.name = name;
		this.lat = lat;
		this.lon = lon;
		this.address = address;		
	}

}
